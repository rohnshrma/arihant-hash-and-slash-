// Primitive Data Types
// 1. String

var yourName = "john";
var yourEmail = "john@email.com";

// 2. Number

var age = 23;
var height = 1.8;

// 3. Boolean

var isIndian = true;
var isEven = false;

// Reference Data Types
// 1. Arrays

var marks = [12, 34, 56, 67];
var fruits = ["Ronit", "John", "Kamal"];

// 2. Objects

var car = {
  model: "i10",
  brand: "Hyundai",
  year: 2020,
};

// Trivial Data Types
// 1. Undefined

var x;
console.log(x); // undefined

// 2. Null

var y = null; // null
